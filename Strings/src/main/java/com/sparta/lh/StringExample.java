package com.sparta.lh;

public class StringExample {
    public static void main(String[] args) {
        String string1 = "Hello";
        string1.toUpperCase();
        System.out.println(string1);

        System.out.println(string1.toCharArray());

        String name = "Liam";
        System.out.println("Hello " + name);
    }
}
